<p><h1>Les épreuves écrites</h1>
    Culture générale et expression : synthèse de documents et écriture personnelle (4 heures – coefficient 2)
    <br>Expression et communication en langue anglaise (2 heures - coefficient 1) :

    <br>rédaction, en français, d’un compte-rendu reprenant les idées essentielles d’un texte ou d’un dossier en langue anglaise
    <br>rédaction en anglais, d’écrits professionnels.

    <br>Mathématiques pour l’informatique : arithmétique, suites numériques, calcul matriciel 2, éléments de la théorie des ensembles, graphes et ordonnancement (2 heures – coefficient 2)
    <br>Analyse économique, managériale et juridique des services informatiques : sujet en 2 parties dont une étude de documents et un questionnement portant sur un thème juridique qui change tous les 2 ans (4 heures – coefficient 3)
    <br>Production et fourniture de services informatiques : étude de cas de production et fourniture de services informatiques, construite à partir d’une situation réelle, dans le domaine de spécialité choisi (4 heures – coefficient 5).
<br>
<h1>Les épreuves orales</h1>
    Conception et maintenance de solutions informatiques : interrogation orale et pratique à partir d'un dossier décrivant deux situations professionnelles (contrôle en cours de formation ou en épreuve finale – coefficient 4)
    <br>Parcours de professionnalisation : interrogation sur l'expérience professionnelle acquise au long des deux années de la formation (contrôle en cours de formation ou en épreuve finale – coefficient 3)
    <br>Algorithmique appliquée : écrire, interpréter et éventuellement modifier ou compléter un ou plusieurs algorithmes (contrôle en cours de formation ou en épreuve finale – coefficient 1)
    <br>Expression et communication en langue anglaise : oral de compréhension incluant des documents audio et vidéo (20 minutes - contrôle en cours de formation ou en épreuve finale – coefficient 1)
</p>


